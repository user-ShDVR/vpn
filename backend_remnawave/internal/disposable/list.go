// IsDisposable reports whether the email's domain is in the blocked set.
// Comparison is case-insensitive on the domain part.
func IsDisposable(emailAddr string) bool {
	at := strings.LastIndex(emailAddr, "@")
	if at < 0 || at == len(emailAddr)-1 {
		return false
	}
	domain := strings.ToLower(emailAddr[at+1:])
	_, ok := blocked[domain]
	return ok
}
